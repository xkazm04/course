// Get persona by ID
export function getPersonaById(id: string): ClientPersona | undefined {
    return PERSONA_TEMPLATES.find(p => p.id === id);
}

// Get personas by archetype
export function getPersonasByArchetype(archetype: PersonaArchetype): ClientPersona[] {
    return PERSONA_TEMPLATES.filter(p => p.archetype === archetype);
}

// Get personas by industry
export function getPersonasByIndustry(industryId: string): ClientPersona[] {
    return PERSONA_TEMPLATES.filter(p => p.industry.id === industryId);
}

// Get all industries
export function getAllIndustries(): Industry[] {
    return Object.values(INDUSTRIES);
}

// Get random persona
export function getRandomPersona(): ClientPersona {
    return PERSONA_TEMPLATES[Math.floor(Math.random() * PERSONA_TEMPLATES.length)];
}
